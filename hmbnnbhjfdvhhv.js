function escapeHTML(str) {
  return str.replace(/&/g, "&amp;")
            .replace(/</g, "&lt;")
            .replace(/>/g, "&gt;")
            .replace(/"/g, "&quot;")
            .replace(/'/g, "&#039;");
}

let name = prompt("Enter your character's name:");
document.getElementById("charName").innerHTML = escapeHTML(name);
